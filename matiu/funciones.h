#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED

/**
* \brief Realiza la suma entre dos numeros
* \param Primer operando a sumar
* \param Segundo operando a sumar
* \return El resultado de la suma
*/
int suma (int x, int y);

/**
* \brief Realiza la resta entre dos numeros
* \param Primer operando a restar
* \param Segundo operando a restar
* \return El resultado de la resta
*/
int resta (int x, int y);

/**
* \brief Realiza la division entre dos numeros
* \param Primer operando a dividir
* \param Segundo operando a dividir
* \return El resultado de la division
*/
int division(int x, int y);

/**
* \brief Realiza la multiplicacion entre dos numeros
* \param Primer operando a multiplicar
* \param Segundo operando a multiplicar
* \return El resultado de la multiplicacion
*/
int multiplicacion(int x, int y);

/**
* \brief Realiza el factorial del primer operando ingresado
* \param Primer operando ingresado al cual se le realizara la operacion factorial
* \return El resultado del factorial
*/
int factorial(int x);

#endif  // FUNCIONES_H_INCLUDED

